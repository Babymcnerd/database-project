
CREATE TABLE IF NOT EXISTS bag ( bagid VARCHAR(255) PRIMARY KEY,
                                                            numberofdiscs INT, bagname VARCHAR(255),
                                                                                       owner VARCHAR(255));


CREATE TABLE IF NOT EXISTS discsinbag ( b1 VARCHAR(255),
                                           b2 VARCHAR(255),
                                              b3 VARCHAR(255),
                                                 b4 VARCHAR(255),
                                                    b5 VARCHAR(255),
                                                       b6 VARCHAR(255),
                                                          b7 VARCHAR(255) );


CREATE TABLE IF NOT EXISTS person ( name VARCHAR(255),
                                         username VARCHAR(255) PRIMARY KEY,
                                                                       password VARCHAR(255) );


CREATE TABLE IF NOT EXISTS company ( name VARCHAR(255) PRIMARY KEY,
                                                               hqlocation VARCHAR(255) );


CREATE TABLE IF NOT EXISTS proplayer ( playerid VARCHAR(255) PRIMARY KEY,
                                                                     ranking INT, name VARCHAR(255),
                                                                                       bagid VARCHAR(255),
                                                                                             companyname VARCHAR(255) );


CREATE TABLE IF NOT EXISTS disc ( speed INT, glide INT, turn INT, fade INT, material VARCHAR(255),
                                                                                     name VARCHAR(255),
                                                                                          discid VARCHAR(255) PRIMARY KEY);


